let result = 1 + 3
//operator tambah
const tambah = result + 1
document.write(tambah) //5
document.write("<br>") //5

//operator pengurangan
const kurang = result - 1
document.write(kurang)
document.write("<br>")

//perkalian
const perkalian = result * 1
document.write(perkalian)
document.write("<br>")

//pembagian
const pembagian = result / 2
document.write(pembagian)
document.write("<br>")

//perpangkatan (eksponen)
const perpangkatan = result ** 2
document.write(perpangkatan)
document.write("<br>")

//modulus (sisa bagi)
const modulus = result % 3
document.write(modulus)
document.write("<br>")