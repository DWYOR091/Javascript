//unary
//+ menandakan nilai positif
//- menandakan nilai negatif
// ++ menandakan menaikan 1 angka
// -- menandakan menurunkan 1 angka

let result = +1
document.write(`<p>${result}</p>`)

//berkurang
result--
document.write(`<p>${result}</p>`)

//increment
result++
document.write(`<p>${result}</p>`)

//decrement
result--
document.write(`<p>${result}</p>`)
